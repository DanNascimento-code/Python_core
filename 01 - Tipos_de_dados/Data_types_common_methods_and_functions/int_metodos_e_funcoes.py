
# .bit_length()
# Mostra quantos bits são necessários para representar o número
x = 10
print(f"metodo bit_length(): {x.bit_length()}")

# abs() | é uma função e não um método
# Valor absoluto
y = 10
print(f"função abs(): {abs(y)}")

# pow() | é uma função e não um método
# potênciação
print(f"função pow(): {pow(2, 3)}")

# divmod | é uma função e não um método
# retorna uma tupla contendo o quociente e o resto da divisão
print(f"função divmod(): {divmod(10, 3)}")

